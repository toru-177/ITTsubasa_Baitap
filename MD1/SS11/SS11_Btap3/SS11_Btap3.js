let number = Number(prompt("Nhập vào một số từ 0 đến 9:"));

switch(number) {
    case 0:
        alert("Không")
        break;
    case 1:
        alert("Một")
        break;
    case 2:
        alert("Hai")
        break;
    case 3:
        alert("Ba")
        break;
    case 4:
        alert("Bốn")
        break;
    case 5:
        alert("Năm")
        break;
    case 6:
        alert("Sáu")
        break;
    case 7:
        alert("Bảy")
        break;
    case 8:
        alert("Tám")
        break;
    case 9:
        alert("Chín")
        break;
    default: alert("Số phải từ 0 đến 9")
        break;
}